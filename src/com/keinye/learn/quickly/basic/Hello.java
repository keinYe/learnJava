// public 访问修饰符，表示是公开的
// class 用来定义一个类
// Hello 类名，通常首字母大写
public class Hello {
	// main 方法名，通常首字母小谢
	// void 方法返回值，这里表示无返回值
	// static 表示该方法为静态方法
	// args 方法能够接收的参数，字符串数组
	public static void main(String[] args) {
		System.out.println("Hello World!");
	}
}
